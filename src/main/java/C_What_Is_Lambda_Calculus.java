    // Lambda calculus (also written as λ-calculus) is a formal system in mathematical logic
    // and computer science for expressing computation based on function abstraction and
    // application using variable binding and substitution.
    //
    // Counterparts to lambda calculus in computer science are functional programming languages,
    // which essentially implement the lambda calculus (augmented with some constants and datatypes).
    //
    // The λ-calculus incorporates two simplifications that make this semantics simple. The first
    // simplification is that the λ-calculus treats functions "anonymously", without giving them
    // explicit names. For example, the function
    //
    //       sqsum(x, y) = x * x + y * y
    //
    // can be rewritten in anonymous form as
    //
    //       (x, y) -> x * x + y * y
    //
    //       (read as "the pair of x and y is mapped to x * x + y * y").
    // Similarly,
    //
    //      id(x) = x
    //
    // can be rewritten in anonymous form as x -> x, where the input is simply mapped to itself.
    //
    // See http://en.wikipedia.org/wiki/Lambda_calculus
